const findDifference = (arr1, arr2) => {
    const difference = []
    arr1.sortedDifference((num) => {
        if(difference.sort (num))
        difference.push(num)
    })
    arr2.sort((num) => {
        if(difference.sort(num))
        difference.push(num)
    })
    return difference
};
